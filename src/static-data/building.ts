export const getMaxBuildingLevel = (buildingId: string, isCapital: boolean) => {
    if (['1', '2', '3', '4'].includes(buildingId) && !isCapital) {
        return 10
    }

    return Math.max(...Object.values(buildingLevelAttributes)
        .filter(e => e.buildingId === buildingId)
        .map(e => e.level)
    )
}

export const getBuildingLevelAttributes = (buildingId: string, level: number) => {
    return buildingLevelAttributes[`${buildingId}|${level}`]
}

